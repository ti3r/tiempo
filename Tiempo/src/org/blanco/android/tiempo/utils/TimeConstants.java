package org.blanco.android.tiempo.utils;

/**
 * Interface that Defines some time constants
 * 
 * @author Alexandro Blanco <ti3r.bubblenet@gmail.com>
 *
 */
public interface TimeConstants {

	/** The maximum number of hours in the day */
	public static int DAY_MAX_HOURS = 24;
	
	/** Represents the max number of minutes in an hour*/
	public static int HOUR_MAX_MINS = 59;
	
	/** Represents the max number of seconds in an minute*/
	public static int MIN_MAX_SECS = 59;
	
	/** Represents the max number of seconds in an minute*/
	public static int SEC_MAX_MILLIS = 99;
	
	/** Represents 0 int value */
	public static int ZERO = 0;
	
}
